<script>
export default {
  props: {
    progress: {
      type: Number,
      required: false,
      default: 0
    },
    live: {
      type: Boolean,
      required: false,
      default: false
    }
  },
  data () {
    return {
      dropInterval: null,
      dropActive: true,
	  dropAnimated: true
    }
  },
  mounted () {
    this.dropInterval = setInterval(this.triggerDrop, 550)
	this.showProgress (this.computedProgress)
  },
  beforeDestroy () {
    clearInterval(this.dropInterval)
  },
  computed: {
	  computedProgress() {
		  if (this.live && this.progress === 0) {
			  return 5
		  }
		  else if (this.progress < 3) {
			  return 0
		  }
		  else if (this.progress > 98) {
			  return 10
		  } else {
			  return Math.round(this.progress/10)
		  }
	  }
  },
  methods: {
	  triggerDrop() {
		  if (this.computedProgress === 10) {
			  this.dropAnimated = false
			  return false
		  }
			this.dropAnimated = true
		  this.dropActive = !this.dropActive
	  },
	  showProgress (stage) {
		const progressId = `step-${stage}`
		const allFrames = document.querySelectorAll(".clock-progress");
		allFrames.forEach(function(userItem) {
			if (userItem.getAttribute('id') === progressId) {
				userItem.style.display = 'block';
			} else {
				userItem.style.display = 'none';
			}
		});
	  }
  },
  watch: {
        computedProgress: function(newValue) {
            this.showProgress (newValue)
        }
    }
}
